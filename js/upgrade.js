let currentLang = localStorage.getItem('lang') || 'vi';

function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  const dict = TRANSLATIONS[lang] || TRANSLATIONS['en'];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.type === 'submit' || el.type === 'button') {
          el.value = dict[key];
        } else {
          el.placeholder = dict[key];
        }
      } else {
        el.innerHTML = dict[key];
      }
    }
  });

  // Update lang toggle button label
  const toggle = document.querySelector('.lang-toggle');
  const labels = { en: '🇬🇧 EN', vi: '🇻🇳 VI', ko: '🇰🇷 한국어' };
  if (toggle) toggle.textContent = labels[lang] || '🇬🇧 EN';

  document.documentElement.lang = lang === 'ko' ? 'ko' : lang === 'vi' ? 'vi' : 'en';

  // Mark active in menu
  document.querySelectorAll('.lang-menu a, .nav__lang button').forEach(el => {
    el.classList.toggle('active', el.dataset.lang === lang);
  });
}

function initLangSystem() {
  const toggle = document.querySelector('.lang-toggle');
  const menu = document.querySelector('.lang-menu');
  if (!toggle || !menu) return;

  toggle.addEventListener('click', (e) => {
    e.stopPropagation();
    menu.classList.toggle('open');
  });

  menu.querySelectorAll('a').forEach(a => {
    a.addEventListener('click', (e) => {
      e.preventDefault();
      applyTranslations(a.dataset.lang);
      menu.classList.remove('open');
      trackEvent('language_switch', { lang: a.dataset.lang });
    });
  });

  document.addEventListener('click', () => menu.classList.remove('open'));
  applyTranslations(currentLang);
}

