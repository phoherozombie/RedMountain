// ── Multi-Step Form ─────────────────────────────────
function initMultiStepForm() {
  const form = document.getElementById('multistep-form');
  if (!form) return;

  let currentStep = 1;
  const totalSteps = 4;
  const formData = {};

  const steps = form.querySelectorAll('.multistep-panel');
  const dots = form.querySelectorAll('.mstep');
  const nextBtns = form.querySelectorAll('[data-next]');
  const backBtns = form.querySelectorAll('[data-back]');
  const success = form.querySelector('.form-success');

  function updateProgress() {
    dots.forEach((dot, i) => {
      dot.classList.remove('active', 'done');
      if (i + 1 < currentStep) dot.classList.add('done');
      if (i + 1 === currentStep) dot.classList.add('active');
    });
    steps.forEach((step, i) => {
      step.classList.toggle('active', i + 1 === currentStep);
    });
  }

  // Option tile selection
  form.querySelectorAll('.option-tile').forEach(tile => {
    tile.addEventListener('click', () => {
      const group = tile.closest('.option-tiles');
      group.querySelectorAll('.option-tile').forEach(t => t.classList.remove('selected'));
      tile.classList.add('selected');
      formData[tile.dataset.key] = tile.dataset.value;

      // Auto-advance after 0.3s
      setTimeout(() => {
        if (currentStep < totalSteps) {
          currentStep++;
          updateProgress();
          trackEvent('multistep_next', { step: currentStep });
        }
      }, 300);
    });
  });

  nextBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      if (currentStep < totalSteps) {
        currentStep++;
        updateProgress();
        trackEvent('multistep_next', { step: currentStep });
      }
    });
  });

  backBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      if (currentStep > 1) {
        currentStep--;
        updateProgress();
      }
    });
  });

  // Final submit (step 4 contact info)
  const submitBtn = form.querySelector('[data-submit]');
  if (submitBtn) {
    submitBtn.addEventListener('click', (e) => {
      e.preventDefault();
      const name = form.querySelector('#ms-name')?.value?.trim();
      const email = form.querySelector('#ms-email')?.value?.trim();
      const message = form.querySelector('#ms-message')?.value?.trim();

      if (!name || !email) {
        alert('Please enter your name and email.');
        return;
      }

      formData.name = name;
      formData.email = email;
      formData.message = message;

      // Build personalised WhatsApp message
      const stayLabels = { '1month': '1 month', '2months': '2 months', '3months': '3 months', 'flexible': 'flexible' };
      const budgetLabels = { 'budget': 'budget', 'mid': 'standard', 'premium': 'premium', 'flexible': 'flexible' };
      const roomLabels = { 'studio': 'Garden Studio', 'onebedroom': 'Horizon One-Bedroom', 'unsure': 'not sure yet' };

      const waText = `Hi! My name is ${formData.name || 'there'}.\n\nI'm interested in a long stay:\n• Duration: ${stayLabels[formData.duration] || 'TBD'}\n• Budget: ${budgetLabels[formData.budget] || 'TBD'}\n• Room: ${roomLabels[formData.room] || 'TBD'}\n\nCan you share availability and pricing?\n\n${message || ''}`.trim();

      // Show WhatsApp link in success state
      const successWA = form.querySelector('#success-wa');
      if (successWA) successWA.href = buildWALink(waText);

      // Hide steps, show success
      steps.forEach(s => s.classList.remove('active'));
      dots.forEach(d => { d.classList.remove('active'); d.classList.add('done'); });
      if (success) success.classList.add('active');

      trackEvent('form_submit', {
        duration: formData.duration,
        budget: formData.budget,
        room: formData.room,
      });
    });
  }

  updateProgress();
}

