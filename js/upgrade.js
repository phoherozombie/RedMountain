// ── Exit Intent Popup ───────────────────────────────
let exitPopupShown = false;

function initExitPopup() {
  const overlay = document.getElementById('exit-popup-overlay');
  if (!overlay) return;

  // Don't show if already seen this session
  if (sessionStorage.getItem('exitPopupSeen')) return;

  let triggered = false;
  const show = () => {
    if (triggered || exitPopupShown) return;
    triggered = true;
    exitPopupShown = true;
    overlay.classList.add('active');
    sessionStorage.setItem('exitPopupSeen', '1');
    trackEvent('exit_popup_shown', { page: window.location.pathname });
  };

  // Desktop: mouse leaves top of viewport
  document.addEventListener('mouseleave', (e) => {
    if (e.clientY < 10) show();
  });

  // Mobile: back button (popstate) or 30sec idle
  const idleTimer = setTimeout(show, 30000);

  // Close handlers
  const closeBtn = document.getElementById('exit-popup-close');
  const skipBtn = document.getElementById('exit-popup-skip');
  const waBtn = document.getElementById('exit-popup-wa');

  const close = () => {
    overlay.classList.remove('active');
    clearTimeout(idleTimer);
  };

  if (closeBtn) closeBtn.addEventListener('click', close);
  if (skipBtn) {
    skipBtn.addEventListener('click', () => {
      close();
      trackEvent('exit_popup_dismissed');
    });
  }
  if (waBtn) {
    waBtn.href = buildWALink("Hi! I saw your website and I'm interested in a long stay at Red Mountain. Can you help?");
    waBtn.addEventListener('click', () => {
      trackEvent('exit_popup_wa_click');
    });
  }

  overlay.addEventListener('click', (e) => {
    if (e.target === overlay) close();
  });
}

